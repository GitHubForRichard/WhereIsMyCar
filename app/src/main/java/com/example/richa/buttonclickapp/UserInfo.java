package com.example.richa.buttonclickapp;

public class UserInfo {

    public String email;
    public String licensePlate;
    public String model;
    public String color;
    public int year;

    public UserInfo(String email, String licensePlate, String model, String color, int year) {
        this.email = email;
        this.licensePlate = licensePlate;
        this.model = model;
        this.color = color;
        this.year = year;
    }
}
